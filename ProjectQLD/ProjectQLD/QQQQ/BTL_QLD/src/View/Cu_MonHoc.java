    private void loadDuLieuLenLuoi() {
        dtm = new DefaultTableModel();
        //dtm.addColumn("Tên Sinh Viên");

        dtm.addColumn("Mã Môn Học");
        dtm.addColumn("Tên Môn Học");
        dtm.addColumn("Tín Chỉ");
        dtm.addColumn("Hệ Số Giữa Kỳ");
        dtm.addColumn("Hệ Số Cuối Kỳ");

        MonHocDAO monHocDAO = new MonHocDAO();
        List<MonHoc> monHocList = monHocDAO.selectAll();
        for (MonHoc monHoc : monHocList) {
            Vector vt = new Vector();
            vt.add(monHoc.getMaMonHoc());
            vt.add(monHoc.getTenMonHoc());
            vt.add(monHoc.getTinChi());
            vt.add(monHoc.getHeSoGiuaKy());
            vt.add(monHoc.getHeSoCuoiKy());
            dtm.addRow(vt);
        }
        tbl_MonHoc.setModel(dtm);
    }

