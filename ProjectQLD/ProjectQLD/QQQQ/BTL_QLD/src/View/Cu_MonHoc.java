    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            if (txtMaMonHoc.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Mã Môn Học");
            } else if (txtTenMonHoc.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Tên Môn Học");
            } else if (txtTinChi.getText().isEmpty() && txtTinChi.getText() != null) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Số Tín Chỉ");
            } else if (txtHeSo_GiuaKy.getText().isEmpty() && txtHeSo_GiuaKy.getText() != null) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Hệ Số Giữa Kỳ");
            } else if (txtHeSo_CuoiKy.getText().isEmpty() && txtHeSo_CuoiKy.getText() != null) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Hệ Số Cuối Kỳ");
            }
            MonHocDAO monHocD = new MonHocDAO();
            MonHoc monHoc = new MonHoc(txtMaMonHoc.getText(), txtTenMonHoc.getText(), Integer.parseInt(txtTinChi.getText()), Float.parseFloat(txtHeSo_GiuaKy.getText()), Float.parseFloat(txtHeSo_CuoiKy.getText()));
            boolean result = monHocD.insert(monHoc);
            boolean checkMH = SQLConnection.Kiem_Tra_Trung("tbl_MONHOC", "MA_MON_HOC", txtMaMonHoc.getText());
            if (result == false) {
                if (checkMH == true) {
                    JOptionPane.showMessageDialog(this, "Thêm Mới Thành Công !");
                } else if (checkMH == false) {
                    JOptionPane.showMessageDialog(this, "Mã Môn Học Đã Tồn Tại, Bạn Vui Lòng Nhập Lại ! ");
                }
            }
            reset();
            loadDuLieuLenLuoi();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrmMonHoc.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FrmMonHoc.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Xóa Không ", "Thông Báo", OK_CANCEL_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int row = tbl_MonHoc.getSelectedRow();
            String maMonHoc = (String) tbl_MonHoc.getValueAt(row, 0);
            MonHocDAO monHocD = new MonHocDAO();
            MonHoc monHoc = new MonHoc(maMonHoc);
            boolean result = monHocD.delete(monHoc);
            if (result == false) {
                JOptionPane.showMessageDialog(this, "Xóa Thành Công !");
            }
            reset();
            loadDuLieuLenLuoi();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        if (txtTenMonHoc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Tên Môn Học");
        } else if (txtTinChi.getText().isEmpty() && txtTinChi.getText() != null) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Số Tín Chỉ");
        } else if (txtHeSo_GiuaKy.getText().isEmpty() && txtHeSo_GiuaKy.getText() != null) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Hệ Số Giữa Kỳ");
        } else if (txtHeSo_CuoiKy.getText().isEmpty() && txtHeSo_CuoiKy.getText() != null) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Hệ Số Cuối Kỳ");
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Cập Nhật Không ", "Thông Báo", OK_CANCEL_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            MonHoc mh = new MonHoc(txtTenMonHoc.getText(), Integer.parseInt(txtTinChi.getText()), Float.parseFloat(txtHeSo_GiuaKy.getText()), Float.parseFloat(txtHeSo_CuoiKy.getText()));
            mh.setMaMonHoc(txtMaMonHoc.getText());
            MonHocDAO mhD = new MonHocDAO();
            boolean result = mhD.update(mh);
            if (result == false) {
                JOptionPane.showMessageDialog(this, "Cập Nhật Thành Công!");
            }
            reset();
            loadDuLieuLenLuoi();
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

