    private void loadDuLieuLenLuoi() {
        dtm = new DefaultTableModel();
        //dtm.addColumn("Tên Sinh Viên");

        dtm.addColumn("Mã Lớp");
        dtm.addColumn("Tên Lớp");
        dtm.addColumn("Tên Khóa");
        dtm.addColumn("Niên Khóa");

        LopDAO lopDAO = new LopDAO();
        List<Lop> lopList = lopDAO.selectAll();
        for (Lop lop : lopList) {
            Vector vt = new Vector();
            vt.add(lop.getMaLop());
            vt.add(lop.getTenLop());
            vt.add(lop.getTenKhoa());
            vt.add(lop.getNienKhoa());
            dtm.addRow(vt);
        }
        tbl_Lop.setModel(dtm);
    }

