    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Xóa Không ", "Thông Báo", OK_CANCEL_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int row = tbl_Lop.getSelectedRow();
            String maLop = (String) tbl_Lop.getValueAt(row, 0);
            LopDAO lopD = new LopDAO();
            Lop lop = new Lop(maLop);
            boolean result = lopD.delete(lop);
            if (result == false) {
                JOptionPane.showMessageDialog(this, "Xóa Thành Công !");
            }
            reset();
            loadDuLieuLenLuoi();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            if (txtMaLop.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Mã Lớp");
            } else if (txtTenLop.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Tên Lớp");
            } else if (txtTenKhoa.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Tên Khóa ");
            } else if (txtNienKhoa.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Niên Khóa ");
            }
            LopDAO lopD = new LopDAO();
            Lop lop = new Lop(txtMaLop.getText(), txtTenLop.getText(), txtTenKhoa.getText(), txtNienKhoa.getText());
            boolean result = lopD.insert(lop);
            boolean checkMH = SQLConnection.Kiem_Tra_Trung("tbl_LOP", "MA_LOP", txtMaLop.getText());
            if (result == false) {
                if (checkMH == true) {
                    JOptionPane.showMessageDialog(this, "Thêm Mới Thành Công !");
                } else if (checkMH == false) {
                    JOptionPane.showMessageDialog(this, "Mã Môn Học Đã Tồn Tại, Bạn Vui Lòng Nhập Lại ! ");
                }
            }
            reset();
            loadDuLieuLenLuoi();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrmLop.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FrmLop.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        if (txtTenLop.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Tên Lớp");
        } else if (txtTenKhoa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Tên Khóa ");
        } else if (txtNienKhoa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Niên Khóa ");
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Cập Nhật Không ", "Thông Báo", OK_CANCEL_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            Lop l = new Lop(txtTenLop.getText(), txtTenKhoa.getText(), txtNienKhoa.getText());
            l.setMaLop(txtMaLop.getText());
            LopDAO lD = new LopDAO();
            boolean result = lD.update(l);
            if (result == false) {
                JOptionPane.showMessageDialog(this, "Cập Nhật Thành Công!");
            }
            reset();
            loadDuLieuLenLuoi();
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

