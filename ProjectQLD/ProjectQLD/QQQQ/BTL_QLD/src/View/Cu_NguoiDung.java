    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String input = txtTimKiem.getText();
        dtm = new DefaultTableModel();

        dtm.addColumn("Tên Đăng Nhập");
        dtm.addColumn("Mật Khẩu");
        dtm.addColumn("Trạng Thái");
        NguoiDungDAO nguoiDungDAO = new NguoiDungDAO();
        List<NguoiDung> diemList = nguoiDungDAO.search(input);
        for (NguoiDung nd : diemList) {
            Vector vt = new Vector();
            vt.add(nd.getTenDangNhap());
            vt.add(nd.getMatKhau());
            vt.add(nd.getTrangThai());

            dtm.addRow(vt);
        }
        tbl_NguoiDung.setModel(dtm);
    }//GEN-LAST:event_btnTimKiemActionPerformed

