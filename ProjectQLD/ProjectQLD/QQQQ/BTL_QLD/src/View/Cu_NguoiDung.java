    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            if (txtTenDangNhap.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Tên Đăng Nhập");
            } else if (txtMatKhau.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Mật Khẩu");
            } else if (cbxTrangThai.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa Chọn Trạng Thái ");
            }

            NguoiDungDAO nguoiDungD = new NguoiDungDAO();
            NguoiDung nguoiDung = new NguoiDung();
            nguoiDung.setTenDangNhap(txtTenDangNhap.getText());
            nguoiDung.setMatKhau(txtMatKhau.getText());
            nguoiDung.setTrangThai(((NguoiDung) cbxTrangThai.getSelectedItem()).getTrangThai());
            boolean result = nguoiDungD.insert(nguoiDung);
            boolean checkMH = SQLConnection.Kiem_Tra_Trung("tbl_NGUOIDUNG", "TEN_DANG_NHAP", txtTenDangNhap.getText());
            if (result == false) {
                if (checkMH == true) {
                    JOptionPane.showMessageDialog(this, "Thêm Mới Thành Công !");
                } else if (checkMH == false) {
                    JOptionPane.showMessageDialog(this, "Mã Môn Học Đã Tồn Tại, Bạn Vui Lòng Nhập Lại ! ");
                }
            }
            reset();
            loadDuLieuLenLuoi();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrmNguoiDung.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FrmNguoiDung.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Xóa Không ", "Thông Báo", OK_CANCEL_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int row = tbl_NguoiDung.getSelectedRow();
            String tenDangNhap = (String) tbl_NguoiDung.getValueAt(row, 0);
            NguoiDungDAO nguoiDungD = new NguoiDungDAO();
            NguoiDung nguoiDung = new NguoiDung(tenDangNhap);
            boolean result = nguoiDungD.delete(nguoiDung);
            if (result == false) {
                JOptionPane.showMessageDialog(this, "Xóa Thành Công !");
            }
            reset();
            loadDuLieuLenLuoi();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        if (txtMatKhau.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Mật Khẩu");
        } else if (cbxTrangThai.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Trạng Thái ");
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Cập Nhật Không ", "Thông Báo", OK_CANCEL_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            NguoiDung nd = new NguoiDung();
            nd.setMatKhau(txtMatKhau.getText());
            nd.setTrangThai(((NguoiDung) cbxTrangThai.getSelectedItem()).getTrangThai());
            nd.setTenDangNhap(txtTenDangNhap.getText());
            NguoiDungDAO lD = new NguoiDungDAO();
            boolean result = lD.update(nd);
            if (result == false) {
                JOptionPane.showMessageDialog(this, "Cập Nhật Thành Công!");
            }
            reset();
            loadDuLieuLenLuoi();
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

