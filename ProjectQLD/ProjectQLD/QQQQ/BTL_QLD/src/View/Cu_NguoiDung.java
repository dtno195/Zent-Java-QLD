    private void loadDuLieuLenLuoi() {
        dtm = new DefaultTableModel();
        //dtm.addColumn("Tên Sinh Viên");

        dtm.addColumn("Tên Đăng Nhập");
        dtm.addColumn("Mật Khẩu");
        dtm.addColumn("Trạng Thái");

        NguoiDungDAO nguoiDungDAO = new NguoiDungDAO();
        List<NguoiDung> nguoiDungList = nguoiDungDAO.selectAll();
        for (NguoiDung nguoiDung : nguoiDungList) {
            Vector vt = new Vector();
            vt.add(nguoiDung.getTenDangNhap());
            vt.add(nguoiDung.getMatKhau());
            vt.add(nguoiDung.getTrangThai());
            dtm.addRow(vt);
        }
        tbl_NguoiDung.setModel(dtm);
    }
    private void loadComboBox() {
        NguoiDungDAO nguoiDungDAO = new NguoiDungDAO();
        List<NguoiDung> nguoiDungList = nguoiDungDAO.selectAll();
        Vector v = new Vector(nguoiDungList);
        DefaultComboBoxModel dcb = new DefaultComboBoxModel(v);
        cbxTrangThai.setModel(dcb);
        cbxTrangThai.setRenderer(new NguoiDungRender());
    }

