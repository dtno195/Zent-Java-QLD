    private void tbl_SinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_SinhVienMouseClicked
        try {
            int row = tbl_SinhVien.getSelectedRow();
            String maSinhVien = (String) tbl_SinhVien.getValueAt(row, 0);
            String tenSinhVien = (String) tbl_SinhVien.getValueAt(row, 1);
            String gioiTinh = (String) tbl_SinhVien.getValueAt(row, 2);
            String ngaySinh = (String) tbl_SinhVien.getValueAt(row, 3);
            String queQuan = (String) tbl_SinhVien.getValueAt(row, 4);
            String sDT = (String) tbl_SinhVien.getValueAt(row, 5);
            String email = (String) tbl_SinhVien.getValueAt(row, 6);
            String maLop = (String) tbl_SinhVien.getValueAt(row, 7);

            txtMaSinhVien.setText(maSinhVien);
            txtTenSinhVien.setText(tenSinhVien);
            if (gioiTinh.equalsIgnoreCase("Nam")) {
                rbtNam.setSelected(true);
            } else {
                rbtNu.setSelected(true);
            }
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            dtcNgaySinh.setDate(ngaySinh != null && ngaySinh != "" ? sdf.parse(ngaySinh) : null);
            txtQueQuan.setText(queQuan);
            txtSoDienThoai.setText(sDT);
            txtEmail.setText(email);
            // txtMaLop.setText(maLop);
            btnCapNhat.setEnabled(true);
            btnThem.setEnabled(false);
            btnXoa.setEnabled(true);
            txtMaSinhVien.setEnabled(false);
            //cbbVaiTro.setSelectedItem(vaiTroDAO.getVaiTroById(maVaiTro));
            LopDAO lopDAO = new LopDAO();
            cbxMaLop.setSelectedItem(lopDAO.getMaLopID(maLop));
            // txtMaLop.setEnabled(false);
        } catch (ParseException ex) {
            Logger.getLogger(FrmSinhVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tbl_SinhVienMouseClicked


