    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String input = txtTimKiem.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        dtm = new DefaultTableModel();

        dtm.addColumn("Mã Sinh Viên");
        dtm.addColumn("Tên Sinh Viên");
        dtm.addColumn("Giới Tính");
        dtm.addColumn("Ngày Sinh");
        dtm.addColumn("Quê Quán");
        dtm.addColumn("Số Điện Thoại");
        dtm.addColumn("Email");
        dtm.addColumn("Mã Lớp");
        SinhVienDAO sinhVienDAO = new SinhVienDAO();
        List<SinhVien> sinhVienList = sinhVienDAO.search(input);
        for (SinhVien sv : sinhVienList) {
            Vector vt = new Vector();
            vt.add(sv.getMaSinhVien());
            vt.add(sv.getTenSinhVien());
            vt.add(sv.getGioiTinh() ? "Nam" : "Nữ");
            vt.add(sv.getNgaySinh() != null ? sdf.format(sv.getNgaySinh()) : "");
            vt.add(sv.getQueQuan());
            vt.add(sv.getSDT());
            vt.add(sv.getEmail());
            vt.add(sv.getMaLop());
            dtm.addRow(vt);
        }
        tbl_SinhVien.setModel(dtm);
    }//GEN-LAST:event_btnTimKiemActionPerformed

