    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        if (txtTenSinhVien.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Tên Sinh Viên");
        } else if (!rbtNam.isSelected() && !rbtNu.isSelected()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Chọn Giới Tính");
        } else if (txtQueQuan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Quê Quán");
        } else if (txtSoDienThoai.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Số Điện Thoại");
        } else if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Email");
        } else if (cbxMaLop.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Chọn Mã Lớp");
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Cập Nhật Không ", "Thông Báo", OK_CANCEL_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            //SinhVien mh = new SinhVien(txtTenSinhVien.getText(), Integer.parseInt(txtSoDienThoai.getText()), Float.parseFloat(txtHeSo_GiuaKy.getText()), Float.parseFloat(txtQueQuan.getText()));
            SinhVien sv = new SinhVien();
            sv.setTenSinhVien(txtTenSinhVien.getText());
            boolean gioiTinh = false;
            if (rbtNam.isSelected()) {
                gioiTinh = true;
            }
            if (rbtNu.isSelected()) {
                gioiTinh = true;
            }
            sv.setGioiTinh(gioiTinh);
            sv.setNgaySinh(dtcNgaySinh.getDate());
            sv.setQueQuan(txtQueQuan.getText());
            sv.setSDT(txtSoDienThoai.getText());
            sv.setEmail(txtEmail.getText());
            sv.setMaLop(((Lop) cbxMaLop.getSelectedItem()).getMaLop());
            sv.setMaSinhVien(txtMaSinhVien.getText());
            SinhVienDAO mhD = new SinhVienDAO();
            boolean result = mhD.update(sv);
            if (result == false) {
                JOptionPane.showMessageDialog(this, "Cập Nhật Thành Công!");
            }
            reset();
            loadDuLieuLenLuoi();
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

