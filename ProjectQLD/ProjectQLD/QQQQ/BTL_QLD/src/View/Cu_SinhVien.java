    private void loadDuLieuLenLuoi() {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        dtm = new DefaultTableModel();
        //dtm.addColumn("Tên Sinh Viên");

        dtm.addColumn("Mã Sinh Viên");
        dtm.addColumn("Tên Sinh Viên");
        dtm.addColumn("Giới Tính");
        dtm.addColumn("Ngày Sinh");
        dtm.addColumn("Quê Quán");
        dtm.addColumn("Số Điện Thoại");
        dtm.addColumn("Email");
        dtm.addColumn("Mã Lớp");

        SinhVienDAO sinhVienDAO = new SinhVienDAO();
        List<SinhVien> sinhVienList = sinhVienDAO.selectAll();
        for (SinhVien sinhVien : sinhVienList) {
            Vector vt = new Vector();
            vt.add(sinhVien.getMaSinhVien());
            vt.add(sinhVien.getTenSinhVien());
            vt.add(sinhVien.getGioiTinh() ? "Nam" : "Nữ");
            vt.add(sinhVien.getNgaySinh() != null ? sdf.format(sinhVien.getNgaySinh()) : "");
            vt.add(sinhVien.getQueQuan());
            vt.add(sinhVien.getSDT());
            vt.add(sinhVien.getEmail());
            vt.add(sinhVien.getMaLop());
            dtm.addRow(vt);
        }
        tbl_SinhVien.setModel(dtm);
    }
    private void loadComboBox() {
        LopDAO lopDAO = new LopDAO();
        List<Lop> lopList = lopDAO.selectAll();
        Vector v = new Vector(lopList);
        DefaultComboBoxModel dcb = new DefaultComboBoxModel(v);
        cbxMaLop.setModel(dcb);
        cbxMaLop.setRenderer(new MaLopRender());

    }
